l=int(input("Enter length of Rectangle: "));
b=int(input("Enter breadth of Rectangle: "));
r=int(input("Enter radius of Circle: "));
a=int(input("Enter side of Square: "));
rectArea=l*b;
rectPerimeter=2*(l+b);
circleArea=3.14*r*r;
circleCircum=2*3.14*r;
squareArea=a*a;
print("Area of Rectangle is: ",rectArea);
print("Perimeter of Rectangle is: ",rectPerimeter);
print("Area of Circle is: ",circleArea);
print("Circumference of Rectangle is: ",circleCircum);
print("Area of Square is: ",squareArea)
